	{
		m_Socket.ShutDown(2);
		m_Socket.Close();
		m_Socket.CancelBlockingCall(); //

		m_ctlListSocket.AddString(_T("ERROR: Failed to connect DMS"));
		m_ctlListSocket.SetCurSel(m_ctlListSocket.GetCount() -1);

		GetDlgItem(IDC_BT_CONNECT)->EnableWindow(TRUE);

		m_bDMSconnected = FALSE;
	}
